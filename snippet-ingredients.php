			<div class="col-6 text-col">
				<h4 class="ingredients-header">If you mix your yoghurt with honey, why do you need the added cane sugar?</h4>
				<p class="ingredients-paragraph">One thing we learned early on in the yoghurt-making process is that it’s an imperfect science. We want every single spoonful of Noosa to taste as good as the last, and to do that we use cane sugar. It helps tone down the natural honey flavor, while ensuring every bite has the absolute perfect amount of sweetness (never too much, never too little).</p>
			</div>
		</div>

