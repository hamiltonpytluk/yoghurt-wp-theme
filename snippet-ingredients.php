			<div class="col-6 text-col">
				<h4 class="ingredients-header">Why do you need stabilizers?</h4>
				<p class="ingredients-paragraph">It takes a lot of work to get the luscious texture our Aussie yoghurt is famous for just right. That’s where gelatin and pectin come in. We use kosher, bovine gelatin and fruit-derived pectin to make sure each batch sets up perfectly, and you know what to expect when you pop the lid. Unfortunately, we realize that the gelatin we use may make Noosa unacceptable for vegetarians and vegans—sorry, mates.</p>
